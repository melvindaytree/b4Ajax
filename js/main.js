function click(clickedElement, targetElement) {
	var tar = document.querySelector(targetElement);
  var cli = document.querySelector(clickedElement);
  
  tar.classList.toggle('hidden');
  cli.addEventListener("click", function hide() {
    tar.classList.toggle('hidden');
    if (tar.classList.contains('hidden')) {
  		cli.innerHTML = "Show Menu";
  	}
    else {
    	cli.innerHTML = "Hide Menu";
    };
  })
  
}


