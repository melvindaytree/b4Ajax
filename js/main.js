/**
3) Create a reusable object function that will represent a book. It should accept a Title, Author, Publication Date, Price, and Status (checkedIn or checkedOut). Create a function called feature() for this object -- it will create a div inside the #featuredBooks div which will display that book's information. Add a CSS class to that indiviudal book's div for checkedIn/checkedOut

**/
function book(title, author, publicationDate, price, status) {
  this.title = title;
  this.author = author; 
  this.publicationDate = publicationDate; 
  this.price = price;
  this.status = status;
  this.feature = function() { 
  
  
  var feat = document.getElementById("featuredBooks");
  
  var createdDiv = document.createElement('div');
  
  
  var createH1 = document.createElement("h1");
  var text = document.createTextNode(this.title);
  createH1.appendChild(text);
  createdDiv.appendChild(createH1);
  
  var createH2 = document.createElement("h2");
  var text2 = document.createTextNode(this.author);
  createH2.appendChild(text2);
  createdDiv.appendChild(createH2);
  
  var createH3 = document.createElement("h3");
  var text3 = document.createTextNode(this.publicationDate);
  createH3.appendChild(text3);
  createdDiv.appendChild(createH3);
  
  var createH4 = document.createElement("h4");
  var text4 = document.createTextNode(this.price);
  createH4.appendChild(text4);
  createdDiv.appendChild(createH4);
  
  var createH5 = document.createElement("h5");
  var text5 = document.createTextNode(this.status);
  createH5.appendChild(text5);
  createdDiv.appendChild(createH5);
  createdDiv.classList.add(this.status);
  
  
  feat.appendChild(createdDiv);
  
           createdDiv.addEventListener("click", function(){
          if (createdDiv.classList.contains("checkedOut")) {
          createdDiv.classList.toggle("checkedOut");
          createdDiv.classList.toggle("checkedIn");
          } else if (createdDiv.classList.contains("checkedIn")) {
          createdDiv.classList.toggle("checkedOut");
          createdDiv.classList.toggle("checkedIn");
          }
        });
}
}


